// ─── Provider ────────────────────────────────────────────────────────────────
export function LanguageProvider({ children }) {
  const [language, setLanguageState] = useState("fr")

  // Load from localStorage on mount
  useEffect(() => {
    const stored = localStorage.getItem("salesbot_lang")
    if (stored === "ar" || stored === "fr") {
      setLanguageState(stored)
    }
    // Default is already "fr"
  }, [])

  // Sync html[lang] and html[dir] whenever language changes
  useEffect(() => {
    const isAr = language === "ar"
    document.documentElement.lang = language
    document.documentElement.dir  = isAr ? "rtl" : "ltr"
  }, [language])

  const setLanguage = useCallback((lang) => {
    if (lang !== "fr" && lang !== "ar") return
    setLanguageState(lang)
    localStorage.setItem("salesbot_lang", lang)
  }, [])

  const t = useCallback(
    (key, vars = {}) => {
      const val = translations[language]?.[key] ?? translations["fr"]?.[key] ?? key
      return Object.entries(vars).reduce(
        (s, [k, v]) => s.replace(`{${k}}`, v),
        val
      )
    },
    [language]
  )

  const isRTL  = language === "ar"
  const dir    = isRTL ? "rtl" : "ltr"
  const locale = language === "ar" ? "ar-MA" : "fr-FR"

  return (
    <LanguageContext.Provider value={{ language, setLanguage, t, dir, isRTL, locale }}>
      {children}
    </LanguageContext.Provider>
  )
}

export const useLanguage = () => useContext(LanguageContext)
